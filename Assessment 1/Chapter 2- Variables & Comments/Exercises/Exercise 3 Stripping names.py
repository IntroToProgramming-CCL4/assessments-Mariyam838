#Define the name with whitespace characters
name= "\t  Isabella Rivers\n"
#Print the original name with whitespace
print("Orignal name:",name)
#Using lstrip() to remove leading whitespace
print("Using lstrip():", name.lstrip())
# Using rstrip() to remove trailing whitespace
print("Using rstrip():", name.rstrip())
# Using strip() to remove both leading and trailing whitespace
print("Using strip():" , name.strip())

